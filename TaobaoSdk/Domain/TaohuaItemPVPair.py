        pythonType = None
        if nameType in nameTypeToPythonType:
            pythonType = nameTypeToPythonType[nameType]
        else:
            pythonType = getattr(
                sys.modules[os.path.basename(
                os.path.dirname(os.path.realpath(__file__))) + "." + nameType], 
                nameType)
        return pythonType
        
